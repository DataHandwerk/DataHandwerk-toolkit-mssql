     repo.Index_IndexSemanticGroup AS T1
     INNER JOIN
     repo.Index__union AS T2
     ON T1.index_guid = T2.index_guid
     INNER JOIN
     repo.RepoObject__gross AS T3
     ON T2.parent_RepoObject_guid = T3.RepoObject_guid
